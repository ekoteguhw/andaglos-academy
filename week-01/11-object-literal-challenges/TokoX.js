const countProfit = function(shoppers) {
	let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];

  let profits = [];
  if (shoppers.length > 0) {
	  let index1 = 0;

	  while (index1 < listBarang.length) {
	  	let barang = listBarang[index1];
	  	let index2 = 0;
	  	let profit = {
		  	product: barang[0],
		  	shoppers: [],
		  	leftOver: barang[2],
		  	totalProfit: 0
		  }

	  	while (index2 < shoppers.length) {
	  		let buyer = shoppers[index2];
	  		
	  		if (barang[0] == buyer.product && barang[2] >= 0) {
	  			if (barang[2] >= buyer.amount) {
	  				profit.shoppers.push(buyer.name);
		  			profit.leftOver -= buyer.amount;
		  			profit.totalProfit += barang[1] * buyer.amount;
		  			barang[2] -= buyer.amount; 
	  			}
	  		}

	  		index2++;
	  	}

	  	profits.push(profit);
	  	index1++;
	  }
	}

  return profits;
}

